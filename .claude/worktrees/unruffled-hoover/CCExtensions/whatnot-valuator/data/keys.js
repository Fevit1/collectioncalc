// Lookup function - find key info for a comic
window.lookupKeyInfo = function(series, issue) {
  if (!series || !issue) return null;
  
  // Normalize series name: lowercase, remove spaces/punctuation
  const normalizedSeries = series.toLowerCase()
    .replace(/[^a-z0-9]/g, '')
    .replace(/^the/, ''); // Remove leading "the"
  
  const issueNum = String(issue).replace(/[^0-9]/g, '');
  
  // Try different key formats (v1 through v10)
  for (let v = 1; v <= 10; v++) {
    const key = `${normalizedSeries}-${issueNum}-v${v}`;
    if (window.COMIC_DATABASE[key]) {
      console.log('[Keys] Found key match:', key, window.COMIC_DATABASE[key].note);
      return window.COMIC_DATABASE[key].note;
    }
  }
  
  // Try without version
  const keyNoVersion = `${normalizedSeries}-${issueNum}`;
  for (const dbKey of Object.keys(window.COMIC_DATABASE)) {
    if (dbKey.startsWith(keyNoVersion + '-')) {
      console.log('[Keys] Found key match:', dbKey, window.COMIC_DATABASE[dbKey].note);
      return window.COMIC_DATABASE[dbKey].note;
    }
  }
  
  return null;
};

